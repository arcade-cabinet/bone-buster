#!/usr/bin/env node
/**
 * IF7 — inventory walker for `references/_extracted/`. Generates
 * `docs/ASSET-INVENTORY.md` with every GLB grouped by category and
 * pack-slug, plus a suggested archetype assignment based on
 * filename heuristics. This is the source of truth that the D5
 * (enemy roster), D7 (per-archetype scatter), and D9 (weapon
 * promotions) sub-pickers draw from.
 *
 * The existing `docs/ASSET_INVENTORY.md` (underscore) is the
 * shipped-asset inventory; this `ASSET-INVENTORY.md` (hyphen) is
 * the candidate-pool inventory.
 *
 * Usage:
 *   pnpm assets:audit-extracted
 *   pnpm assets:audit-extracted --category=psx
 */

import { readdirSync, statSync, writeFileSync } from "node:fs";
import { dirname, join, relative, resolve, sep } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const SRC = join(ROOT, "references", "_extracted");
const OUT = join(ROOT, "docs", "ASSET-INVENTORY.md");

const argv = process.argv.slice(2);
const catArg = argv.find((a) => a.startsWith("--category="));
const categoryFilter = catArg
	? new Set(
			catArg
				.slice("--category=".length)
				.split(",")
				.map((s) => s.trim().toLowerCase()),
		)
	: null;

// Filename → suggested-archetype heuristics. Multi-match keeps all
// hits so the D7 picker can see which archetypes a candidate plays
// well in. Returns a sorted array of archetype tags.
const ARCHETYPE_HINTS = [
	[/\b(corridor|hall|tunnel|industrial|wire|cable|pipe|machinery)\b/i, "corridor"],
	[/\b(arena|coliseum|stadium|pit|ring)\b/i, "arena"],
	[/\b(courtyard|garden|tree|plant|nature|fountain|outdoor)\b/i, "courtyard"],
	[/\b(sewer|water|drain|mud|sludge|fish|swamp)\b/i, "sewer"],
	[/\b(library|book|shelf|paper|library|mansion|study|desk|chair)\b/i, "library"],
];

function archetypeHints(name) {
	const hits = new Set();
	for (const [pat, tag] of ARCHETYPE_HINTS) if (pat.test(name)) hits.add(tag);
	return [...hits].sort();
}

function* walkGlb(dir) {
	let entries;
	try {
		entries = readdirSync(dir, { withFileTypes: true });
	} catch {
		return;
	}
	for (const e of entries) {
		const full = join(dir, e.name);
		if (e.isDirectory()) yield* walkGlb(full);
		else if (e.isFile() && e.name.toLowerCase().endsWith(".glb")) yield full;
	}
}

function packSlugOf(glbAbs) {
	// Normalize Windows backslashes to POSIX before splitting — `relative()`
	// returns platform-specific separators.
	const rel = relative(SRC, glbAbs).split(sep).join("/");
	const parts = rel.split("/");
	return { category: parts[0] ?? "misc", pack: parts[1] ?? "(root)" };
}

try {
	statSync(SRC);
} catch {
	console.error(
		`audit-extracted-assets: ${SRC} missing. Run IF6 (\`pnpm assets:bulk-fbx-to-glb\`) first.`,
	);
	process.exit(1);
}

const candidates = [];
for (const glb of walkGlb(SRC)) {
	const { category, pack } = packSlugOf(glb);
	if (categoryFilter && !categoryFilter.has(category)) continue;
	const rel = relative(SRC, glb).split(sep).join("/");
	const filename = rel.split("/").pop() ?? rel;
	const size = statSync(glb).size;
	candidates.push({ rel, filename, size, category, pack, hints: archetypeHints(filename) });
}

// Group by category → pack
const byCat = new Map();
for (const c of candidates) {
	if (!byCat.has(c.category)) byCat.set(c.category, new Map());
	const packs = byCat.get(c.category);
	if (!packs.has(c.pack)) packs.set(c.pack, []);
	packs.get(c.pack).push(c);
}

function humanSize(n) {
	if (n < 1024) return `${n} B`;
	if (n < 1024 * 1024) return `${(n / 1024).toFixed(1)} KB`;
	return `${(n / (1024 * 1024)).toFixed(1)} MB`;
}

const today = new Date().toISOString().slice(0, 10);
const lines = [
	"---",
	"title: References/extracted asset candidate inventory",
	`updated: ${today}`,
	"status: current",
	"domain: ops",
	"---",
	"",
	"# Candidate asset inventory (`references/_extracted/`)",
	"",
	`${candidates.length} GLB candidate(s) across ${byCat.size} categor${byCat.size === 1 ? "y" : "ies"}. Generated by \`pnpm assets:audit-extracted\` from the IF6 bulk-conversion output.`,
	"",
	"This is the candidate pool the D5 / D7 / D9 hand-pickers draw from. To ship an asset into the production runtime, copy it into `public/assets/models/{enemies,weapons,props}/` and add the URL to `src/assets/models.ts` (which routes through `A()` for BASE_URL correctness).",
	"",
	"Archetype hints are filename heuristics, not contracts. Use them to skim; verify the actual mesh before slotting.",
	"",
	"For the **shipped** asset inventory (what's actually wired in `src/models.ts` today), see [`ASSET_INVENTORY.md`](./ASSET_INVENTORY.md).",
	"",
];

const sortedCats = [...byCat.keys()].sort();
for (const cat of sortedCats) {
	const packs = byCat.get(cat);
	const packCount = packs.size;
	const glbCount = [...packs.values()].reduce((n, list) => n + list.length, 0);
	lines.push(
		"",
		`## ${cat} — ${packCount} pack${packCount === 1 ? "" : "s"}, ${glbCount} GLB${glbCount === 1 ? "" : "s"}`,
		"",
	);
	const sortedPacks = [...packs.keys()].sort();
	for (const pack of sortedPacks) {
		const items = packs.get(pack).sort((a, b) => a.filename.localeCompare(b.filename));
		const packSize = items.reduce((n, c) => n + c.size, 0);
		lines.push(
			`### ${pack} — ${items.length} GLB${items.length === 1 ? "" : "s"}, ${humanSize(packSize)}`,
			"",
		);
		lines.push("| File | Size | Archetype hints |");
		lines.push("| --- | --- | --- |");
		for (const c of items) {
			const hints = c.hints.length ? c.hints.join(", ") : "_(none — heuristic miss)_";
			lines.push(`| \`${c.rel}\` | ${humanSize(c.size)} | ${hints} |`);
		}
		lines.push("");
	}
}

writeFileSync(OUT, lines.join("\n"));
console.log(`Wrote ${OUT} (${candidates.length} GLBs, ${byCat.size} categories)`);
